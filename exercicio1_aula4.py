numero= int(input('Digite um número inteiro qualquer:'))

quadrado= float(numero**2)
print('O quadrado de',numero,'é',quadrado)

raizquadrada= float (numero**(1/2))
print('A raiz quadrada de',numero,'é',raizquadrada)

divisao_qua_raiz= float (quadrado/raizquadrada)
print('A divisão de',quadrado,'por',raizquadrada,'é',divisao_qua_raiz)

